def generate(out_path: Path) -> None:
    # 加载一个无衬线 16px 系统字体（用于 ASCII）
    # macOS 自带字体；Linux 上可改成 /usr/share/fonts/...
    candidates = [
        "/System/Library/Fonts/STHeiti Medium.ttc",   # macOS
        "/System/Library/Fonts/Helvetica.ttc",
        "/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf",  # Debian/Ubuntu
        "/usr/share/fonts/truetype/liberation/LiberationSans-Regular.ttf",
        "C:/Windows/Fonts/arial.ttf",
    ]
    font_path = next((p for p in candidates if Path(p).exists()), None)
    if font_path:
        font = ImageFont.truetype(font_path, 16)
        print(f"[font] 使用字体: {font_path}")
    else:
        font = ImageFont.load_default()
        print("[font] 警告：未找到 TTF，使用默认位图字体（质量差）")

    out = bytearray()
    # ASCII 区
    for code in range(ASCII_START, ASCII_END):
        ch = chr(code)
        out.extend(render_text_glyph(font, ch))
    # 图标区
    for name in ICONS:
        out.extend(render_icon(name))

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_bytes(bytes(out))
    print(f"[ok] 生成 {out_path}: {len(out)} 字节, "
          f"{NUM_ASCII} ASCII + {NUM_ICONS} 图标")


if __name__ == "__main__":
    out = Path(sys.argv[1] if len(sys.argv) > 1 else "data/fonts/ui_font_16.bin")
    generate(out)
