"""生成 16×16 点阵 UI 字模 (1 bit/pixel, 32 bytes/glyph)。

布局：
  [0..95]    ASCII 可打印字符  !"#$%&'()*+,-./...
  [96..111]  自定义图标区（16 个状态栏图标）

每个字模 32 字节，行优先，高位在前：
  byte 0   = 第 0 行像素 (bit15..bit0 = x15..x0)
  byte 1   = 第 1 行像素
  ...
  byte 31  = 第 15 行像素

使用方法：
  python3 scripts/generate_ui_font.py data/fonts/ui_font_16.bin
"""
from __future__ import annotations
