from pathlib import Path

from PIL import Image, ImageDraw, ImageFont


# ASCII 可打印字符 (32..126)
ASCII_START = 0x20
ASCII_END = 0x7F  # exclusive
NUM_ASCII = ASCII_END - ASCII_START  # 95

# 自定义图标（16 个）
ICON_BASE = 0x80
ICONS = [
    "battery_full", "battery_75", "battery_50", "battery_25",
    "wifi_connected", "wifi_disconnected", "charging",
    "sunny", "cloudy", "rainy", "book", "clock",
    "arrow_left", "arrow_right", "check", "cross",
]
NUM_ICONS = len(ICONS)

GLYPH_W = 16
GLYPH_H = 16
GLYPH_BYTES = (GLYPH_W * GLYPH_H) // 8  # 32


def render_text_glyph(font: ImageFont.FreeTypeFont, ch: str) -> bytes:
    """渲染单个 ASCII 字符为 16×16 1bpp 数据（行优先，高位在前）。"""
    # 灰度渲染后取阈值
    img = Image.new("L", (GLYPH_W, GLYPH_H), 255)
    draw = ImageDraw.Draw(img)
    # 字符 bbox 用于居中
    try:
        bbox = draw.textbbox((0, 0), ch, font=font)
        tw, th = bbox[2] - bbox[0], bbox[3] - bbox[1]
        ox = (GLYPH_W - tw) // 2 - bbox[0]
        oy = (GLYPH_H - th) // 2 - bbox[1]
        draw.text((ox, oy), ch, font=font, fill=0)
    except Exception:
        # 空格等不可渲染字符：留空
        pass

    # 转为位图
    data = bytearray(GLYPH_BYTES)
    for y in range(GLYPH_H):
        for x in range(GLYPH_W):
            pixel = img.getpixel((x, y))
            if pixel < 128:
                byte_idx = y * 2 + (x // 8)
                bit_idx = 7 - (x % 8)
                data[byte_idx] |= (1 << bit_idx)
    return bytes(data)


